class Esmalte {
    constructor(Id, marca, nome, cor, tipoDeEsmalte, dataValidade, quantasVezesUsado, posicaoNaLista) {
        this.Id = Id;
        this.marca = marca;
        this.nome = nome;
        this.cor = cor;
        this.tipoDeEsmalte = tipoDeEsmalte;
        this.dataValidade = dataValidade;
        this.quantasVezesUsado = quantasVezesUsado;

        this.posicaoNaLista = posicaoNaLista;
    }
}
